public class ejercicio6 {
//ArrayList ofrece un mejor rendimiento en acceso aleatorio (mediante índices). 
	//Como ArrayList internamente utiliza un arreglo para almacenar los elementos, acceder a un elemento por su índice es una operación de tiempo 
	//constante O(1).
	//ArrayList es más eficiente en la recuperación de elementos específicos debido a su estructura de datos basada en arrays. 
	//Esto significa que es más rápido obtener o modificar elementos en función de su índice.
	
//LinkedList puede ser más eficiente que ArrayList en operaciones de inserción y eliminación en posiciones intermedias de la lista. 
	//Esto se debe a que LinkedList no necesita realocar memoria o mover elementos contiguos como ArrayList, 
	//ya que cada elemento en LinkedList está enlazado con el siguiente y el anterior mediante referencias.
	//En particular, LinkedList es más eficiente en la inserción y eliminación de elementos al principio o al final de la lista. 
	//Estas operaciones tienen complejidad O(1) para LinkedList, mientras que en ArrayList pueden ser O(n) si se necesita realocar memoria 
	//y mover elementos.	
}
